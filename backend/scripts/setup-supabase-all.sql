  ADD CONSTRAINT messages_content_length CHECK (length(content) <= 524288),       -- 512KB
  ADD CONSTRAINT messages_metadata_length CHECK (
    metadata_json IS NULL OR length(metadata_json) <= 1048576                     -- 1MB
  );

