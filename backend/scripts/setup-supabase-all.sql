-- ==================== ARCHIVES & CHAT ====================

create table if not exists public.archives (
  id bigint generated by default as identity primary key,
  owner_user_id text,
  name text not null check (length(trim(name)) between 1 and 120),
  topic text not null check (length(trim(topic)) between 3 and 300),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.conversations (
  id bigint generated by default as identity primary key,
  owner_user_id text,
  archive_id bigint not null references public.archives(id) on delete restrict,
  title text not null check (length(trim(title)) between 1 and 200),
  created_at timestamptz not null default now()
);

alter table public.archives
  add column if not exists owner_user_id text;

alter table public.conversations
  add column if not exists owner_user_id text;

create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  conversation_id bigint not null references public.conversations(id) on delete cascade,
  role text not null check (role in ('user', 'assistant', 'system')),
  content text not null,
  metadata_json text,
  run_id text,
  run_status text,
  run_phase text,
  run_last_heartbeat_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists public.archive_contexts (
  archive_id bigint primary key references public.archives(id) on delete cascade,
  summary text not null default '',
  updated_at timestamptz not null default now()
);

create table if not exists public.archive_research_angles (
  archive_id bigint primary key references public.archives(id) on delete cascade,
  angles_json text not null default '[]',
  meta_json text not null default '{}',
  updated_at timestamptz not null default now()
);

create table if not exists public.archive_intelligence_profiles (
  id bigint generated by default as identity primary key,
  archive_id bigint not null unique references public.archives(id) on delete cascade,
  agenda_text text,
  committee_type text,
  agenda_class text,
  primary_dimensions text,
  completed_divisions text,
  evidence_registry text,
  debate_utility_log text,
  dimension_engine_hash text,
  session_count integer,
  updated_at timestamptz not null default now()
);

create index if not exists conversations_archive_id_created_at_idx
  on public.conversations (archive_id, created_at);

create index if not exists archives_owner_user_id_created_at_idx
  on public.archives (owner_user_id, created_at);

create index if not exists conversations_owner_archive_created_at_idx
  on public.conversations (owner_user_id, archive_id, created_at);

create index if not exists messages_conversation_id_created_at_idx
  on public.messages (conversation_id, created_at);

-- ==================== CACHE TABLE ====================

CREATE TABLE IF NOT EXISTS public.cache_entries (
  id BIGSERIAL PRIMARY KEY,
  namespace TEXT NOT NULL DEFAULT 'default',
  key TEXT NOT NULL,
  value_jsonb JSONB NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  expires_at TIMESTAMPTZ NOT NULL,
  source_hash TEXT,
  agenda_fingerprint TEXT,
  mode TEXT,
  run_tags JSONB,
  UNIQUE(namespace, key)
);

CREATE INDEX IF NOT EXISTS idx_cache_entries_lookup
  ON cache_entries(namespace, key, expires_at);

CREATE INDEX IF NOT EXISTS idx_cache_entries_expires
  ON cache_entries(expires_at);

CREATE INDEX IF NOT EXISTS idx_cache_entries_namespace
  ON cache_entries(namespace);

-- ==================== ROW LEVEL SECURITY ====================

alter table public.archives enable row level security;
alter table public.conversations enable row level security;
alter table public.messages enable row level security;
alter table public.archive_contexts enable row level security;
alter table public.archive_research_angles enable row level security;
alter table public.archive_intelligence_profiles enable row level security;
alter table public.cache_entries enable row level security;

-- ==================== GRANTS ====================

grant select, insert, update, delete on table
